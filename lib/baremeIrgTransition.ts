const BORNE_BASSE = TABLE_IRG_TRANSITION[0][0]; // 30010
const BORNE_HAUTE = TABLE_IRG_TRANSITION[TABLE_IRG_TRANSITION.length - 1][0]; // 34990
const PAS = 10; // les points de la table sont espaces de 10 DA

/**
 * Retourne l'IRG net officiel (DA) pour une base imposable donnee, en
 * interpolant lineairement entre les deux points encadrants de la table
 * (les valeurs officielles progressent de facon tres reguliere tous les
 * 10 DA, donc l'erreur d'interpolation est negligeable, de l'ordre du
 * centime de dinar).
 *
 * Ne doit etre appelee que si 30000 < base <= 35000 environ -- sinon la
 * formule standard (calculerIrgBrut + abattement) s'applique et est deja
 * exacte.
 */
export function irgNetZoneTransition(baseImposable: number): number {
  if (baseImposable <= BORNE_BASSE) {
    return TABLE_IRG_TRANSITION[0][1];
  }
  if (baseImposable >= BORNE_HAUTE) {
    return TABLE_IRG_TRANSITION[TABLE_IRG_TRANSITION.length - 1][1];
  }

  const indexBas = Math.floor((baseImposable - BORNE_BASSE) / PAS);
  const [baseBas, irgBas] = TABLE_IRG_TRANSITION[indexBas];
  const [baseHaut, irgHaut] = TABLE_IRG_TRANSITION[Math.min(indexBas + 1, TABLE_IRG_TRANSITION.length - 1)];

  if (baseHaut === baseBas) return irgBas;

  const fraction = (baseImposable - baseBas) / (baseHaut - baseBas);
  return irgBas + (irgHaut - irgBas) * fraction;
}
